import joblib
import pandas as pd
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.svm import LinearSVC
from sklearn.model_selection import train_test_split

def train_nlp_model(df):

    # FIX 1: Ensure text column is string
    df['title'] = df['title'].astype(str)

    # FIX 2: Remove empty values
    df = df[df['title'].str.strip() != ""]

    X = df['title']
    y = df['genre_encoded']

    vectorizer = TfidfVectorizer(
        max_features=3000,
        stop_words='english',
        min_df=2  # prevents empty vocab issue
    )

    X_vec = vectorizer.fit_transform(X)

    X_train, X_test, y_train, y_test = train_test_split(
        X_vec, y, test_size=0.2, stratify=y, random_state=42
    )

    model = LinearSVC()
    model.fit(X_train, y_train)

    joblib.dump(model, "models/nlp_model.pkl")
    joblib.dump(vectorizer, "models/tfidf_vectorizer.pkl")

    return model